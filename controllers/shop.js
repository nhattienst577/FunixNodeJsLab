exports.getCart = (req, res, next) => {
  Cart.getCart((cart) => {
    Product.fetchAll((products) => {
      const cartProducts = [];
      //lặp lại tất cả sp ktra xem sp có được lưu trữ trong giỏ hàng không
      for (product of products) {
        //cartProductData lưu trữ mảng sp trong dữ liệu sp giỏ hàng và sau đó ta sẽ kiểm tra sự tồn tại của sp đó
        const cartProductData = cart.products.find(
          (prod) => prod.id === product.id
        );
        if (cartProductData) {
          cartProducts.push({ productData: product, qty: cartProductData.qty });
        }
      }
      res.render("shop/cart", {
        path: "/cart",
        pageTitle: "Your Cart",
        products: cartProducts,
      });
    });
  });
};

exports.postCart = (req, res, next) => {
  const prodId = req.body.productId; //productId la name trong product-detail
  Product.findById(prodId, (product) => {
    //product được truy xuất từ csdl của sp, khi có dữ liệu có thể sd để cập nhật lại
    Cart.addProduct(prodId, product.price);
  });
  res.redirect("/cart");
};

exports.postCartDeleteProduct = (req, res, next) => {
  const prodId = req.body.productId;
  Product.findById(prodId, (product) => {
    Cart.deleteProduct(prodId, product.price);
    res.redirect("/cart");
  });
};

